// Generates src/resources/boond-db-schema.ts from the public BoondManager
// database documentation (https://doc.boondmanager.com/bddboondmanager/).
//
// Run once (and re-run when Boond updates its schema doc):
//   node scripts/generate-db-schema.mjs
//
// The output feeds the local `boond_extractbi_schema` tool so the model never
// needs to fetch the web docs (no network, no permission prompt) when writing
// ad-hoc SQL for `boond_extractbi_query`.

import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const BASE = "https://doc.boondmanager.com/bddboondmanager";
const OUT = join(dirname(fileURLToPath(import.meta.url)), "..", "src", "resources", "boond-db-schema.ts");
const CONCURRENCY = 8;
const DESC_MAX = 160;

function decodeEntities(s) {
  return s
    .replace(/&#x27;/g, "'")
    .replace(/&quot;/g, '"')
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&nbsp;/g, " ");
}

function stripTags(s) {
  return decodeEntities(s.replace(/<[^>]*>/g, " "))
    .replace(/\s+/g, " ")
    .trim();
}

async function fetchText(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${res.status} ${res.statusText} on ${url}`);
  return res.text();
}

// 1) Table list from the index sidebar (links to classes/TAB_X.html)
const index = await fetchText(`${BASE}/`);
const tables = [...new Set([...index.matchAll(/classes\/(TAB_[A-Z0-9_]+)\.html/g)].map((m) => m[1]))].sort();
console.log(`${tables.length} tables found`);

// 2) Parse each class page
async function parseTable(name) {
  const html = await fetchText(`${BASE}/classes/${name}.html`);

  // Table-level description: first <div class="description"> before the first property block.
  const firstProp = html.indexOf('class="property item"');
  const head = firstProp > 0 ? html.slice(0, firstProp) : html;
  const tableDescMatch = head.match(/<div class="description">([\s\S]*?)<\/div>/);
  const tableDesc = tableDescMatch ? stripTags(tableDescMatch[1]).slice(0, DESC_MAX) : "";

  // Property blocks
  const columns = {};
  const blockRe = /<div id="property_([A-Z0-9_]+)" class="property item">([\s\S]*?)(?=<div id="property_|$)/g;
  for (const m of html.matchAll(blockRe)) {
    const col = m[1];
    const block = m[2];
    const type = stripTags(block.match(/<span class="type">([\s\S]*?)<\/span>/)?.[1] ?? "");
    let desc = stripTags(block.match(/<div class="description">([\s\S]*?)<\/div>/)?.[1] ?? "");
    // Drop the boilerplate "This property can be null." sentence to save space.
    desc = desc.replace(/This property can be\s+null\s*\.?/gi, "").replace(/\s+/g, " ").trim();
    // Foreign-key hint: any TAB_X referenced in the description.
    const refs = [...new Set([...desc.matchAll(/TAB_[A-Z0-9_]+/g)].map((r) => r[0]))].filter((r) => r !== name);
    if (desc.length > DESC_MAX) desc = desc.slice(0, DESC_MAX - 1) + "…";
    const parts = [type, desc].filter(Boolean).join(" — ");
    columns[col] = refs.length > 0 ? `${parts} [→ ${refs.join(", ")}]` : parts;
  }
  return { d: tableDesc, c: columns };
}

const schema = {};
let done = 0;
for (let i = 0; i < tables.length; i += CONCURRENCY) {
  const batch = tables.slice(i, i + CONCURRENCY);
  const results = await Promise.all(
    batch.map(async (t) => {
      try {
        return [t, await parseTable(t)];
      } catch (err) {
        console.warn(`FAILED ${t}: ${err.message}`);
        return null;
      }
    })
  );
  for (const r of results) {
    if (r) schema[r[0]] = r[1];
  }
  done += batch.length;
  process.stdout.write(`\r${done}/${tables.length}`);
}
console.log();

// 3) Emit the TS module
const banner = `// AUTO-GENERATED by scripts/generate-db-schema.mjs — do not edit by hand.
// Source: ${BASE}/ (parsed table & column reference for ad-hoc ExtractBI SQL).
// Re-generate when BoondManager updates its schema documentation.

/** table → { d: table description, c: { column → "Type — description [→ FK targets]" } } */
export const BOOND_DB_SCHEMA: Record<string, { d: string; c: Record<string, string> }> = `;
writeFileSync(OUT, banner + JSON.stringify(schema, null, 1) + ";\n");
const kb = Math.round(Buffer.byteLength(JSON.stringify(schema)) / 1024);
console.log(`Wrote ${OUT} (${Object.keys(schema).length} tables, ~${kb} KB)`);
